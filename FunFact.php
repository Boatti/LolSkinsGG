<h3>5. The BF Sword Is an Allusion to Doom’s BFG9000</h3>
<p>The BF Sword in League of Legends is a fairly expensive basic item that does about 50 attack damage. Maybe it fetches a premium price because it’s got that “90s cool” attitude straight from Doom’s most over-the-top weapon.</p>

<h3>6. Aatrox Was Inspired by Lord of the Rings Characters</h3>
<p>Mike Laygo, a senior animator on League of Legends, stated that Aatrox’s animations were inspired by Balrog and Sauron. Lago himself has previously worked on Lord of the Rings games.</p>

