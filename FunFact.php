<p>Everyone loves the adorable Poros, and they’re even cuter with a giant black moustache. Simply feed it Poro-Snax while playing as Braum and the cute little critter will sprout some facial hair to match its burly benefactor.</p>

<h3>2. Shaco is an anagram of Chaos</h3>
